        <div class="center">
          <h2>What tips/tricks/suggestions do you have for teachers about to begin the interview process?</h2>
        </div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar1.png"><div class="quote right">Some schools will ask you to do a demonstration lesson to observe how you interact with students. Be prepared for this or to talk about how you'd teach a particular lesson to students with specific learning challenges.</div></div>
        <div class="gaw-quote-row"><div class="quote">Most of the time interviewers are looking for how well you will fit into their school setting. Research the school a little to see if you can find anything about them. What their mission statement is, what things they have been recognized for, what kind of demographics they have, any special projects they are working on.</div><img class="gaw-avatar right" src="img/gaw-avatar2.png"></div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar3.png"><div class="quote right">The first question usually is “tell me about yourself.” Make sure you have a great answer to this question. This is the place you can show them about yourself and your personality.</div></div>
        <div class="gaw-quote-row"><div class="quote">They will probably ask you about your teaching style, how you use technology, and how you engage all students in your class. In older grades where it’s content specific, they might ask questions specifically related to your content area. Use your student teaching and other classroom experiences to help you answer questions.</div><img class="gaw-avatar right" src="img/gaw-avatar4.png"></div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar5.png"><div class="quote right">Also, bring copies of your resume (about 10 to be safe). If you have an electronic portfolio put a link on your resume for them to access it later.</div></div>
        <div class="gaw-quote-row"><div class="quote">Be prepared to answer questions about behavior/ classroom management too. They also might ask about how you plan on communicating with families.</div><img class="gaw-avatar right" src="img/gaw-avatar6.png"></div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar7.png"><div class="quote right">If you had any negative experiences with student teaching, try not to talk about them in a negative way. Instead put a positive twist on it.</div></div>
        <div class="gaw-quote-row"><div class="quote">The best piece of advice I can offer is to be reflective of your practice. Whether you are new to the profession or have been in the classroom for extended amounts of time, be thoughtful!</div><img class="gaw-avatar right" src="img/gaw-avatar8.png"></div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar9.png"><div class="quote right">Anticipate the questions. Usually, interviewers are not trying to fool you and will ask you some pretty direct questions. The most common ones I remember being asked are: "Tell us about the best lesson you ever taught" or "Describe your discipline policy/How would you handle a challenging student" or "What is your philosophy of education?"</div></div>
        <div class="gaw-quote-row"><div class="quote">Practice, practice, practice! Come up with a response to common questions and practice saying it (in front of the mirror!) until you feel comfortable with your answer. Write it down if you need to.</div><img class="gaw-avatar right" src="img/gaw-avatar10.png"></div>
        <div class="gaw-quote-row"><img class="gaw-avatar" src="img/gaw-avatar11.png"><div class="quote right">Research the school and come ready with questions. You should treat every interview like it was your DREAM job, and you should ask specific questions about the culture of the school, the diversity of the student body, the philosophies and values of the principal, etc.</div></div>
